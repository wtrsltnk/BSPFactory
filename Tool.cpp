// Tool.cpp: implementation of the CTool class.
//
//////////////////////////////////////////////////////////////////////

#include "Tool.h"
#include "string.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CTool::CTool(const char* tool)
{
	strcpy(toolName, tool);
	toolPath[0] = '\0';
}

CTool::~CTool()
{
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
const char* CTool::GetToolName()
{
	return this->toolName;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
const char* CTool::GetToolPath()
{
	return this->toolPath;
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CTool::SetToolName(char* toolName)
{
	if (!toolName)
		return;

	strncpy(this->toolName, toolName, 64);
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CTool::SetToolPath(char* toolPath)
{
	if (!toolPath)
		return;

	strncpy(this->toolPath, toolPath, 256);
}
