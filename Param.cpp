// Param.cpp: implementation of the CParam class.
//
//////////////////////////////////////////////////////////////////////

#include "Param.h"
#include "string.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

CParam::CParam(int ID)
{
	this->ID = ID;
	this->value[0] = '\0';
}

CParam::~CParam()
{
}

//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
int CParam::GetID()
{
	return ID;
}
	
//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
const char* CParam::GetValue()
{
	return value;
}
	
//////////////////////////////////////////////////////////////////////
// Purpose : 
// Input   : 
// Output  : 
//////////////////////////////////////////////////////////////////////
void CParam::SetValue(char* value)
{
	if (!value)
		return;

	strncpy(this->value, value, 64);
}